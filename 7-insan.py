import cv2
import numpy as np
img = np.zeros((600, 600, 3), np.uint8)
cv2.ellipse(img, (300, 120), (80, 100), 0, 0, 360, (120, 50, 200), 4)
cv2.line(img, (300, 220), (300, 400), (120, 50, 150), 4)
cv2.line(img, (300, 400), (400, 500), (120, 50, 150), 4)
cv2.line(img, (300, 400), (200, 500), (120, 50, 150), 4)
cv2.line(img, (300, 250), (200, 300), (120, 50, 150), 4)
cv2.line(img, (300, 250), (400, 300), (120, 50, 150), 4)
cv2.ellipse(img, (260, 100), (15, 10), 0, 0, 360, (120, 50, 100), 4)
cv2.ellipse(img, (340, 100), (15, 10), 0, 0, 360, (120, 50, 100), 4)
cv2.ellipse(img, (300, 160), (30, 20), 0, 0, 180, (120, 50, 100), 4)
cv2.imshow("resim", img)
cv2.waitKey(0)
cv2.destroyAllWindows()
